<h2 ><strong>I NOSTRI STUDENTI</strong></h2><hr><br>
<p>Gli studenti, senza distinzione di opinioni politiche, di opzioni culturali e di fede religiosa hanno il diritto di frequentare i corsi della Libera Università della Svizzera e senza distinzione di opinioni politiche, di opzioni culturali e di fede religiosa hanno il diritto di fruire di spazi di socialità, studio e confronto collettivo messi a disposizione dell’Università.</p>
<p>Gli studenti hanno il dovere di rispettare gli spazi messi a loro disposizione dall'Università e di mantenerne la funzionalità e il decoro.</p>
<p>Gli studenti disabili hanno diritto a partecipare attivamente e pienamente a tutte le fasi della vita universitaria</p>
<p>Agli studenti stranieri iscritti Libera Università della Svizzera garantisce, anche con appositi uffici, adeguata assistenza agli studenti stranieri, contribuendo a rimuovere gli ostacoli al loro completo inserimento nella comunità universitaria.</p>
<p>Gli studenti hanno diritto a una valutazione imparziale tramite prove d’esame trasparenti e coerenti con il programma, gli obiettivi formativi e le modalità di svolgimento dei corsi. Le prove d'esame devono essere sostenute con una commissione presieduta da un docente.</p>
<p>Gli studenti hanno altresì diritto, su loro richiesta, a essere esaminati dal docente titolare del corso.</p>
<p>Ogni insegnamento deve prevedere un numero e una distribuzione temporale degli appelli tali da garantire agli studenti una ragionevole programmazione del proprio carico di esami. Il calendario degli appelli deve essere reso per un tempo sufficiente prima dell’inizio delle sessioni d’esame. Le date non possono essere anticipate rispetto al calendario previsto.</p>
<p>È preciso dovere del titolare del corso garantire la propria presenza nelle date di appello da lui indicate. In caso di impossibilità a presenziare per ragioni urgenti e impreviste è suo compito assicurare la presenza di altro docente.</p>
<p>La prova d’esame deve comunque vertere sul programma del corso, opportunamente divulgato, salva restando la possibilità di contenuti specifici per i frequentanti. Il programma del corso e i relativi contenuti devono essere trattati in modo completo nei testi indicati dal docente.</p>
<p>Gli studenti hanno il diritto di conoscere l’esito delle prove d’esame scritte, nonché a ricevere spiegazioni sui criteri di correzione e di valutazione.</p>
<p>Di fronte alla comprovata impossibilità di partecipare agli appelli d’esame regolamentari per gravi ragioni, lo studente ha il diritto di concordare con il docente titolare altra data d’esame nell'ambito della stessa sessione.</p>
<p>E' dovere dello studente sostenere l'esame nel pieno rispetto del lavoro e delle competenze del docente e della specificità della sede universitaria.</p>
<p>Gli studenti hanno diritto a essere ricevuti personalmente dai docenti, per chiarimenti e consigli didattici nonché per essere assistiti nello svolgimento della tesi di laurea e di altri lavori di ricerca concordati. Il docente dà pubblica comunicazione dell’orario di ricevimento da lui stabilito prima dell’inizio di ogni periodo didattico. Dell' eventuale assenza per ragioni urgenti e impreviste egli dà tempestiva comunicazione sul proprio sito telematico.</p>
<p>Gli studenti hanno diritto a un’adeguata attività di orientamento e tutorato. L’attività di tutorato non può svolgersi di regola negli stessi orari delle lezioni.</p>
<p>E' stretto dovere dello studente svolgere la tesi attraverso la propria personale applicazione al tema prescelto, nel pieno rispetto delle norme che regolano il diritto d'autore e la proprietà delle opere d'ingegno di ogni livello.</p>
<h2><strong>TIROCINANTI E STUDENTI IN STAGE</strong></h2><hr><br>
<p>La Libera Università della Svizzera si impegna a promuovere stage e tirocini in armonia con le finalità didattiche indicate dai propri organismi competenti, tenendo conto dei tempi di studio e delle condizioni socioeconomiche dello studente.</p>
<p>Le convenzioni con le agenzie e con gli enti deputati a ospitare le esperienze di stage e tirocinio vengono stipulate nel rispetto della disciplina propria dell’Ente.</p>
<p>Le attività di stage o tirocinio devono essere svolte di regola nei giorni feriali e in orario diurno, e in ogni caso per un monte ore corrispondente al numero di crediti indicato nell'ordinamento didattico in corso.</p>
<p>Gli studenti hanno diritto a valutare le strutture e le attività di stage e tirocinio attraverso questionari distribuiti al termine dell’attività. I risultati dei questionari riferiti ad ogni struttura o reparto ospitante, esaminati dalla Libera Università della Svizzera, vengono resi pubblici.</p>
<p>La valutazione delle attività di stage e tirocinio svolte dagli studenti si riferisce esclusivamente agli obiettivi raggiunti nel corso delle stesse. Tali obiettivi vengono certificati dai docenti e dai responsabili delle strutture ospitanti al termine di un periodo prestabilito dalle competenti strutture didattiche.</p>
<p>Le attività lavorative svolte dagli studenti-lavoratori vengono riconosciute come attività di tirocinio o di stage purché attinenti alle finalità didattiche del Corso di Laurea di appartenenza, previo parere della apposita Commissione della Libera Università della Svizzera competente e previa verifica individuale degli obiettivi raggiunti.</p>
<p>Il riconoscimento può avvenire in ogni caso nei limiti massimi previsti dalla legge e dai regolamenti didattici dei Corsi di Laurea.</p>
<h2><strong>DOTTORANDI DI RICERCA</strong></h2><hr><br>
<p>Le garanzie previste sopra previste per gli studenti, si applicano anche agli studenti di dottorato di ricerca e della formazione post-laurea.</p>
<p>Gli studenti di dottorato hanno diritto all’assegnazione di un tutor da parte del collegio dei docenti che lo segua e lo indirizzi nel percorso di formazione alla ricerca. Per ciascuno studente di dottorato deve essere definito un percorso formativo chiaro, costruito su date certe, con l'indicazione di un argomento di ricerca da sviluppare in autonomia.</p>
<p>È compito del collegio dei docenti individuare e promuovere i modi per finanziare esperienze formative esterne alla Libera Università della Svizzera.</p>
<p>Lo studente di dottorato ha diritto a usufruire di periodi di sospensione per malattia, per maternità o per lavoro. Egli ha altresì diritto a chiedere un breve rinvio dell’esame finale di dottorato qualora ritenesse necessario un approfondimento della tematica di ricerca.</p>
