    <p class="mt-3">
      Il Corso di Laurea Triennale Online in Ingegneria Civile <b>ha come ambito di interesse le costruzioni</b> (edifici civili ed industriali, grandi opere quali ponti, dighe, gallerie, ecc.) e le infrastrutture (vie e trasporti, sistemi di raccolta, di distribuzione e di smaltimento delle acque, ecc.). In tale ambito, <b>l'Ingegnere Civile</b> si occupa della progettazione e della costruzione delle opere, e ne cura l'esercizio, la manutenzione, il rilevamento e il controllo. In anni recenti, nella matrice comune del settore Ingegneria Civile si è andata configurando in misura crescente la richiesta di figure professionali specialistiche, quali ad esempio l'ingegnere per l'ambiente ed il territorio o l'ingegnere edile, cui vengono ora dedicate offerte formative proprie.
    </p>
    <p>
      <b>Principali obiettivi formativi del Corso di Laurea in Ingegneria Civile sono:</b>
    </p>
    <ul>
      <li>assicurare allo studente una adeguata padronanza di metodi e contenuti scientifici generali e di base, con particolare riferimento alla capacità di utilizzare tale conoscenza per interpretare e descrivere i problemi nell'ambito dell'Ingegneria Civile;
      </li>
      <li>
      assicurare specifiche conoscenze professionali preordinate all'inserimento dei laureati nel mondo del lavoro.
      </li>
    </ul>
    <p>
      Il profilo professionale è quello di un Ingegnere Civile che sia in grado di operare in uno degli ambiti di tradizionale competenza (progettazione architettonica, progettazione di strutture ed infrastrutture, progettazione e gestione di opere idrauliche e per l'ingegneria sanitaria).
    </p>
    <p>
      L'offerta didattica, pur essendo pienamente allineata agli obiettivi generali della classe delle Lauree di Ingegneria Civile (L7), è più specificamente mirata alla formazione di figure professionali emergenti nel settore dell'Ingegneria Civile. Allo scopo, l'organizzazione didattica mira ad assicurare una conoscenza di metodi, tecniche e strumenti aggiornati, che consentano di:
    </p>
    <ul>
      <li>progettare e realizzare strutture di medio-piccola dimensione;</li>
      <li>progettare e gestire strutture di Ingegneria Idraulica di dimensioni medio-piccole;</li>
      <li>dimensionare e gestire impianti di Ingegneria Sanitaria Ambientale;</li>
      <li>intervenire nella progettazione e nella realizzazione di infrastrutture di trasporto;</li>
      <li>avere le conoscenze di base per la realizzazione di rilievi geometrici.</li>
    </ul>
    <p>
      <b>Il percorso formativo del laureato in Ingegneria Civile si articola su tre livelli:</b>
    </p>
      <ul>
        <li>formazione di base a carattere generale nell'ambito della matematica, della fisica, della statistica e dell'ingegneristica;</li>
        <li>formazione di natura caratterizzante nelle discipline dell'ingegneria delle strutture, con particolare riferimento alla Scienza ed alla Tecnica delle Costruzioni;</li>
        <li>formazione di natura caratterizzante, finalizzata alla creazione di specifici profili professionali che il corso di studi intende formare, con attività formative che coinvolgono prevalentemente i settori dell'estimo e della geologia.
        </li>
      </ul>
