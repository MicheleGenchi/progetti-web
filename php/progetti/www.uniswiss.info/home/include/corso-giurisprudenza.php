    <h3>Corso di Laurea (online) in Scienze Giuridiche e dell'Organizzazione</h3>
    -->
    <img src="images/giurisprudenza.jpg" alt="" class="img-fluid mt-3">
    <p class="mt-3">
    <b>Il corso fornisce la preparazione culturale e giuridica di base e le capacità pratiche degli operatori giuridici di imprese private e pubbliche.</b><br>
    Scopo del corso è la formazione di un giurista in grado di utilizzare e applicare il diritto, le scienze economiche e linguistiche nella gestione delle risorse umane, nei rapporti di lavoro tra le imprese. <b>Il percorso formativo ha lo scopo</b> di fornire la conoscenza delle regole giuridiche che governano l'organizzazione e il funzionamento dell'impresa, nonché le relazioni che essa instaura con altri operatori economici e con le istituzioni.
    </p>
    <p>
    I laureati in Scienze Giuridiche e dell'Organizzazione devono possedere il dominio dei principali saperi afferenti all'area giuridica, in particolare negli ambiti privatistico, pubblicistico, amministrativo, penale e commerciale, nonché in quelli comparatistico, economico e comunitario. Essi devono essere in grado di individuare ed acquisire autonomamente soluzioni tecniche-giuridiche da utilizzare in contesti professionali, anche in collaborazione con esperti di altre discipline, analizzare i rapporti fra istituzioni ed enti nazionali e comunitari, operare attraverso l'uso di strumenti informatici per la comunicazione e la gestione dell'informazione. L'obiettivo è, dunque, quello di fornire una preparazione multidisciplinare, nonché assicurare un livello culturale adeguato a consentire al laureato l'esercizio di molteplici funzioni giuridico-amministrative e giuridico-economiche-legali, sia nel mondo delle imprese che in quello delle organizzazioni pubbliche e private, nazionali e internazionali. Al fine formativo indicato, il Corso di laurea promuove la capacità di utilizzare almeno due lingue dell'Unione Europea (diverse dall'italiano) per comunicare, in forma orale e scritta, informazioni sia generali, che specialistiche.
    </p>
    <p>
    <b>Attraverso moduli specialistici</b> attivati nell’ambito delle varie discipline, la scelta delle materie opzionali e della prova finale sarà possibile caratterizzare i percorsi formativi dei singoli studenti sulla base dei loro interessi specifici e obiettivi professionali. Tali percorsi comprendono, inoltre, un adeguato periodo di tirocinio professionale e l'acquisizione di abilità relazionali e di contesto.
    </p>
    <p>
    Nel complesso, dunque, <b>l’ offerta formativa del Corso di laurea</b>, risultante dalla presenza degli insegnamenti di base, caratterizzanti, opzionali, di moduli di approfondimento e di tirocini, consente una solida preparazione nei saperi che costituiscono il patrimonio consolidato del giurista, senza trascurare le conoscenze specifiche richieste dai fenomeni di trasformazione delle scienze delle organizzazioni conseguenti ai profondi mutamenti in corso nello scenario economico e politico internazionale. Di tali mutamenti, inoltre, l’offerta formativa consente di cogliere anche la dimensione storica.</p>
