            <li><strong>MISSION</strong></li>
        </ol>
        <p>Il corso ha l’obiettivo di approfondire la materia della tutela dei dati personali, un tema di fondamentale importanza e di grande attualità, specialmente in questi ultimi anni in cui abbiamo assistito ad una crescita esponenziale del diritto alla privacy: una lente di ingrandimento su questo tema, determinata principalmente dallo sviluppo delle nuove tecnologie e dal traffico di dati, cui siamo sempre più sottoposti.
            Se da un lato esiste un diritto all’informazione, dall’altro esiste un diritto alla tutela dei propri dati, a maggior ragione al giorno d’oggi in cui, ai dati, è riconosciuto un enorme valore, anche in termini economici.
            Il percorso formativo, modulare, sinergico tra indirizzi, si articola in:
        <ul>
            <li>ore di perfezionamento;</li>
            <li>ore di formazione;</li>
            <li>ore di aggiornamento;</li>
            <li>seminari;</li>
        </ul>

        <p>e ogni altra iniziativa scientifica ritenuta utile alla formazione e alla qualifica dei partecipanti interattiva alle specifiche richieste del mercato professionale.
            Il compito di direzione, gestione e programmazione compete esclusivamente al Coordinatore del Corso.</p>

        <ol start="8">
            <li><strong>OBIETTIVI</strong></li>
        </ol>
        <p>La Comunità Europea negli ultimi tempi ha varato il GDPR e la Nazioni stanno via via prendendo sempre più coscienza di una opportuna regolamentazione. La pandemia è stata un duro banco di prova e molte sono le falle che si sono evidenziate.
            Gli attacchi informatici, il furto di dati, l’uso massiccio della rete, i dati sanitari. Il mondo deve adeguarsi velocemente alle nuove realtà ed il diritto non può perdere tempo prezioso. La conoscenza del mondo del web è ancora poco diffusa nella collettività, ma ai giorni nostri diventa indispensabile l’uso delle nuove tecnologie.
            Prestare il consenso, subire un furto di dati e, per le aziende, il traffico dei big data o il verificarsi di un data breach, sono temi all’ordine del giorno. La consapevolezza resta fondamentale e la formazione del giurista, in questi settori, è indispensabile, essendo problematiche sempre più all’ordine del giorno.
            Il mondo del web va studiato, esaminato, spesso non vi è una soluzione giurisprudenziale, ma occorre competenza e discernimento per applicare o proporre le giuste soluzioni ed interpretazioni. Nei prossimi anni, non solo la giurisprudenza, ma anche il potere legislativo saranno sempre più impegnati nella regolamentazione di questa materia. L’obiettivo, pertanto, del presente corso è ambizioso: fornire strumenti tecniche e capacità di ragionamento in un settore tutto in divenire.
            Il Corso si propone di approfondire questi temi e conferire ai discenti conoscenze teoriche sul diritto alla privacy, approfondimenti tematici sulle prevalenti discipline introdotte dal GDPR e analisi delle pronunce giurisprudenziali in materia.
            Le competenze acquisite potranno essere utilizzate nella libera professione e nei campi della Pubblica Amministrazione.</p>

