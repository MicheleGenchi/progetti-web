            <li><strong>PRESENTAZIONE DEL CORSO</strong></li>
        </ol>
        Le recenti modifiche ordinamentali hanno radicalmente modificato lo <em>status quo ante</em> dell’impianto normativo dei Beni Culturali e del Turismo.
        Se il Ministero dei Beni Culturali istituito nel 1974 grazie a Giovanni Spadolini, già all’atto del suo momento genetico, è stato definito da parte della dottrina <em>“una scatola vuota”</em>, il Ministero del Turismo si è contraddistinto per la sua totale assenza o per le sue inadeguate annessioni.
        Con la modifica del Titolo V della Costituzione del 2001 moltissime funzioni relative ai Beni Culturali ed al Turismo sono state devolute alle Regioni.
        Addirittura fino allo scorso anno il Ministero della Cultura era definito MiBACT, acronimo che circoscriveva il Ministero per i Beni Culturali ed il Turismo.
        Quest’ultimo veniva collocato quindi in posizione ancillare rispetto ai Beni Culturali e letto nella sua unica accezione di turismo culturale.
        La delega al turismo, oltretutto, per un breve lasso di tempo, è stata addirittura annessa al Ministero dell’agricoltura con l’espressa intenzione di tutelare il <em>made in Italy</em>.
        L’aspetto storico dei due temi, oltre alle connesse dinamiche socio- economiche, saranno ampiamente argomentati dal Corso <em>de quo.</em></p>
        Il significativo cambio di rotta degli ultimi anni sarà, inoltre, esaminato approfonditamente nel corso delle lezioni.
        Recentemente abbiamo infatti assistito, con acrobatico cambio di rotta, alla costituzionalizzazione ed istituzione, con dignità di portafoglio, del Ministero del Turismo.
        Quindi, la nuova struttura, oltre alla sua vocazione culturale, è stato dischiusa ad una dimensione prospettica più ampia.
        Quindi, non solo turismo culturale ma, approccio turistico nelle sue diverse accezioni concettuali: enogastronomiche, balneari, religiose, sportive ecc.
        Ritornando al Ministero della Cultura si assiste ad una rivoluzione del regime sanzionatorio rispetto ai reati specifici commessi ai danni dei Beni Culturali.
        E’, infatti,&nbsp; imminente presso la Camera dei Deputati l’approvazione definitiva della riforma complessiva della materia, un rinnovato corpus normativo che oltre ad introdurre nuove fattispecie criminose specificamente rivolte ai Beni culturali renderà meno blando e più rigoroso l’aspetto sanzionatorio.
        La tematica punitiva, ad oggi oggetto di approssimativa previsione del Codice dei Beni culturali diventerà, invero, parte integrante del Codice Penale.&nbsp;
        Questo tema rappresenterà il fulcro ed il momento qualificante di un intero modulo del Corso di perfezionamento in Diritto dei Beni Culturali e Turismo.</p>

