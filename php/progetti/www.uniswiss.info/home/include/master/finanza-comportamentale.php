        <p>La finanza comportamentale si occupa di finanza economica e psicologia; quindi, indaga gli schemi dei comportamenti sociali e individuali nei mercati.
        La finanza comportamentale, quindi, analizza quali siano le modalità di scelta in campo finanziario, in che modo l’irrazionalità e l’istinto influiscano in un campo estremamente analitico e statistico come la finanza, e come i dati statistici influenzino a loro volta l’irrazionalità e l’istinto. Nel mercato finanziario è possibile trovare trader che fanno lunghe analisi statistiche prima di prendere una posizione su un determinato asset ed altri che si fidano solo del proprio istinto. Grazie alla finanza comportamentale è possibile analizzare quali siano le conseguenze di queste modalità di scelta negli investimenti, nelle reazioni del mercato e della distribuzione delle risorse, anche grazie all’integrazione di analisi e studi di psicologia cognitiva.
        il Corso di alta formazione in Finanza Comportamentale, mira a formare un professionista altamente specializzato in possesso di conoscenze economiche e psicologiche, che è in grado di operare nell&#8217;ambito delle scelte di investimento.</p>

