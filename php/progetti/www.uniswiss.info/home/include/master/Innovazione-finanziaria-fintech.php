        <p>Il Corso di Alta Formazione in “<em>Financial Technology end Innovation”</em> si concentra su sei moduli individuati per fornire abilità analitiche chiave per la conoscenza del settore finanziario e necessari per avere un quadro completo del fenomeno dell’innovazione in atto. Il Corso aiuta ad acquisire le conoscenze necessarie per il processo decisionale in finanza aziendale, così come a capire la meccanica del settore finanziario, che è in continua evoluzione per l'innovazione tecnologica, legata alla diffusione delle piattaforme digitali, della tecnologia della blockchain e dell'intelligenza artificiale. Le competenze che vengono acquisite sono richieste dai migliori reclutatori nella finteched gli elementi appresi sono necessari per intraprendere una carriera orientata al <em>fintech</em> nel settore dei servizi finanziari.</p>
        <!-- /wp:paragraph -->

        <!-- wp:paragraph -->
        <p>Al completamento del Corso si è in grado di formulare strategie finanziarie utilizzando gli strumenti più innovativi e, dunque, risolvere i problemi di gestione del risparmio come appaiono in situazioni di vita reale. &nbsp;Il Corso è rivolto ai laureati in economia e giurisprudenza. Tuttavia, è aperto a tutti coloro che vogliono migliorare le proprie capacità finanziarie e le conoscenze in materia.</p>
        <!-- /wp:paragraph -->

        <!-- wp:paragraph -->
        <p>Il corso può essere preceduto da due moduli facoltativi: - uno corso base di informatica e, - uno di lingua tecnica inglese.</p>
        <!-- /wp:paragraph -->

        <!-- wp:paragraph -->
        <p><strong>I Moduli</strong></p>
        <!-- /wp:paragraph -->

        <!-- wp:list {"ordered":true} -->
        <ol><!-- wp:list-item -->
            <li><strong>I prodotti finanziari innovativi (Dott.ssa Elena Repman)</strong><!-- wp:list -->
                <ul><!-- wp:list-item -->
                    <li>I nuovi canali di finanziamento sono visti come soluzioni in competizione tra loro o con i canali più tradizionali, ma in realtà essi convivono con gli strumenti tradizionali. Proprio per questo è importante approfondirli perché può non esistere una risposta univoca alla domanda: “qual è lo strumento di finanziamento migliore per l’esigenza di sviluppo di un’impresa?”. Conoscere gli strumenti finanziari, le loro principali caratteristiche e capire quali si adattino meglio alla necessità aziendale è il primo passo per sfruttare al meglio queste nuove opportunità, crescere ed essere sempre più competitivi in un mercato in continua e veloce trasformazione.</li>
                    <!-- /wp:list-item -->
                </ul>
                <!-- /wp:list -->
            </li>
            <!-- /wp:list-item -->

            <!-- wp:list-item -->
            <li><strong>Nuove modalità di Finanziamento per <em>startup</em> e piccole e medie imprese (Dott. Enea Franza)</strong><!-- wp:list -->
                <ul><!-- wp:list-item -->
                    <li>In tale modulo vengono enucleati i concetti di base e le principali fasi del ciclo di evoluzione delle PMI e parallelamente analizzata l’integrazione ottimale dei processi innovativi finanziari nelle PMI. Il modulo offre una panoramica completa di come il <em>Fintech</em> può offrire nuove opportunità per lo sviluppo delle PMI e nello specifico a quelle appena costituite, ovvero, le  <em>start-up</em>. Il modulo illustrerà, inoltre, le modalità di finanziamento attraverso il <em>crowdfunding</em>, che è oggi tra le forme più efficaci di finanziamento di progetti imprenditoriali e delle ICO’s, ovvero, di finanziamento attraverso i <em>Tokens</em>.</li>
                    <!-- /wp:list-item -->
                </ul>
                <!-- /wp:list -->
            </li>
            <!-- /wp:list-item -->

            <!-- wp:list-item -->
            <li><strong>Borsa Italiana e il mercato dell’AIM ( Dott.ssa Catia Maietta)</strong><!-- wp:list -->
                <ul><!-- wp:list-item -->
                    <li>AIM Italia é un mercato gestito da Borsa Italiana sul quale vengono scambiati i titoli azionari di piccole e medie imprese basate in Italia; Tale mercato, è una grande opportunità per gli imprenditori italiani ed esteri, in quanto la quotazione su un tale mercato offre l'accesso ad un <em>network </em>formato da investitori istituzionali (fondi d'investimento) specializzati nel segmento delle small e medium cap. Tuttavia esso è ancora non ben conosciuto e il modulo ha lo scopo di approfondirne il funzionamento e come realizzare un’operazione di quotazione.</li>
                    <!-- /wp:list-item -->
                </ul>
                <!-- /wp:list -->
            </li>
            <!-- /wp:list-item -->

            <!-- wp:list-item -->
            <li><strong>Smart Contract  (dott. William De Nonnis)</strong><!-- wp:list -->
                <ul><!-- wp:list-item -->
                    <li>In tale modulo impareremo a conoscere i <em>smart contract</em>, che allo stesso modo di un contratto tradizionale - ma utilizzando la tecnologia <a href="https://www.money.it/Blockchain-significato-cos-e-come-funziona"><em>blockchain</em></a> - stabiliscono regole e penali di un accordo. Essi fornisco senz’altro uno strumento sempre più indispensabile per aiutare le persone a scambiare denaro, trasferire proprietà e qualsiasi altra cosa di valore in modo trasparente e senza ricorrere ai servizi di un intermediario e costituiscono un terreno di importante conoscenza operativa.</li>
                    <!-- /wp:list-item -->
                </ul>
                <!-- /wp:list -->
            </li>
            <!-- /wp:list-item -->

            <!-- wp:list-item -->
            <li><strong>Sicurezza: Quale futuro? (Dott. Valerio Ferlizi)</strong><!-- wp:list -->
                <ul><!-- wp:list-item -->
                    <li>Il mondo della sicurezza si sta evolvendo rapidamente verso un’offerta che racchiude sia prodotti tecnologicamente all’avanguardia ma anche attraverso procedure sempre più complesse di cui troppo spesso non se ne comprendono affondo le infinite opportunità. In altri termini il modulo ha come obbiettivo affrontare la “questione sicurezza” a 360 gradi, garantendo una valutazione sensata della sicurezza e la valutazione di scelte opportune per una corretta implementazione anche tecnologica dell’impresa.</li>
                    <!-- /wp:list-item -->
                </ul>
                <!-- /wp:list -->
            </li>
            <!-- /wp:list-item -->

            <!-- wp:list-item -->
            <li><strong>L’evoluzione delle nuove figure professionali (Avv. Paolo Patrizio)</strong><!-- wp:list -->
                <ul><!-- wp:list-item -->
                    <li>Il mondo del lavoro sta cambiando e vive sempre più di contaminazione. Il digitale non solo sta incoraggiando la nascita di nuove figure professionali, ma sta anche trasformando le professioni già esistenti in chiave tecnologica. Il modulo analizza i cambiamenti del lavoro, analizzando le nuove figure professionali legate alla tecnologia. E’ d’evidenza che i giovani ed il lavoro sono due entità di notevole importanza che devono incontrarsi e che la conoscenza del nuovo mondo del lavoro è indispensabile per impostare una formazione utile al fine di una corretta calibrazione della propria formazione.</li>
                    <!-- /wp:list-item -->
                </ul>
