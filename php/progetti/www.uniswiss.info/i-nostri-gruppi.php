<h2 ><strong>I NOSTRI GRUPPI DI LAVORO</strong></h2><hr><br>
<p>La Libera Università della Svizzera promuove la formazione di <strong>gruppi di lavoro</strong> che condividono l’obiettivo, il metodo, le risorse. Tutti i gruppi di lavoro condividono in principi ispiratori della Libera Università della Svizzera che sono i principi di democrazia, di libertà, di eguaglianza e di pluralismo.</p>
<p>I nostri gruppi di lavoro sono accumunati dallo scopo che è costituito dall’oggetto di studio e di ricerca che è individuato dal comitato scientifico de la Libera Università della Svizzera.</p>
<p>I gruppi di lavoro nascono da decisione de Libera Università della Svizzera ed il comitato dei capi dipartimento ne assicura il coordinamento.</p>
<p>Il Gruppo di Studio promuoverà le seguenti attività:</p>
<ul>
<li>organizzare incontri, coinvolgendo anche esperti nazionali e internazionali, finalizzati al confronto tra i partecipanti;</li>
<li>promuovere seminari e convegni, rivolti anche alle istituzioni e agli operatori del mercato;</li>
<li>produrre volumi e articoli scientifici.</li>
</ul>
<p>I membri sono scelti tra le personalità internazionali che ruotano nell’ambito del <em>network</em> de la Libera Università della Svizzera e che, al momento della costituzione del gruppo di lavoro, perseguono ricerche ed iniziative coincidenti con l’obiettivo della ricerca assegnata al gruppo.</p>
<p>Gli esperti, dunque, sono assegnati al gruppo per le loro competenze ed esperienze, in tal modo contribuendo a definire il tipo e la qualità del lavoro di gruppo. Per questo, il momento dell’individuazione dei componenti è fondamentale, non può essere che competenza de Libera Università della Svizzera. La strategia ed i piani di azione della ricerca sono lasciati, invece, al gruppo di lavoro che, al suo seno, al solo fine di coordinamento ha un coordinatore del gruppo.</p>
<p>Il metodo di lavoro dei gruppi lavoro si riferiscono sia alle&nbsp;modalità tecniche&nbsp;di realizzazione delle attività, che a quelle&nbsp;relazionali&nbsp;tra i componenti del gruppo. Attenendo tale attività all’operatività ed&nbsp;alle interazioni&nbsp;tra i membri, anche tale attività è attribuita al coordinatore del gruppo, che risponde del lavoro e dei risultati della ricerca del team di fronte alla Libera Università della Svizzera.</p>
<p>La Libera Università della Svizzera non ritiene, infatti, di sindacare le modalità operative dei gruppi di lavoro nella convinzione che non esista un metodo corretto valido per tutti i gruppi di lavoro.</p>
<p>Tuttavia, si richiede al coordinatore del gruppo di lavoro di attenersi a criteri di adeguatezza&nbsp;(ovvero, di rispetto all’obiettivo ed ai compiti del gruppo) di coerenza&nbsp;(con le risorse a disposizione e con i vincoli della situazione) e di&nbsp;corrispondenza&nbsp;con le attese e i desideri dei componenti del gruppo. Inoltre, il coordinatore del gruppo assicurerà la scorretta e completa gestione dei flussi di informazione tra i membri.</p>
<p>Il&nbsp;setting, con questo termine intendo sia il&nbsp;luogo&nbsp;o i luoghi&nbsp;di lavoro, che a&nbsp;strumentazione&nbsp;sono messi a disposizione de La Libera Università della Svizzera. Il coordinatore del gruppo di lavoro potrà organizzare, previo accordo con i componenti del gruppo, i lavori come meglio crede</p>
<p>I gruppi di lavoro devono raggiungere l’obiettivo entro un termine stabilito dalla Libera Università della Svizzera.</p>
<p>Attualmente sono costituiti presso la Libera Università della Svizzera i seguenti gruppi di lavoro:</p>
