        <p>L’apprendimento delle lingue straniere è oggi assolutamente indispensabile, ma non apprendono una nuova
            lingua allo stesso modo. E ai nostri giorni, nell’era della globalizzazione, sapere almeno una lingua è
            diventato sempre più importante. Le persone hanno bisogno di comunicare in diverse lingue per motivi
            professionali, accademici e di viaggio.&nbsp;</p>



        <p>Per questo motivo la Libera università della Svizzera segue diversi metodi di apprendimento, facendo
            riferimento a vari metodi di insegnamento, in quanto com’è noto ogni metodo di insegnamento si basa su una
            particolare visione della comprensione della lingua o dei processi di apprendimento, spesso facendo ricorso
            a tecniche e materiali specifici utilizzati in una sequenza predefinita.</p>



        <p>Di seguito ti elenco le principali metodologie secondo l’ordine&nbsp;<strong>cronologico</strong>&nbsp;del
            loro sviluppo:</p>



        <ul>
            <li>Il metodo classico, detto anche Metodo&nbsp;<strong>grammaticale-traduttivo&nbsp;</strong></li>



            <li>Metodo&nbsp;<strong>diretto</strong>&nbsp;– con al centro l’importanza della conversazione</li>



            <li><strong>Audio-orale</strong>, diretto sull’ascolto</li>



            <li>Insegnamento&nbsp;<strong>umanistico-affettivo</strong>&nbsp;– una gamma di metodi olistici applicati
                all’insegnamento delle lingue</li>



            <li>Metodo&nbsp;<strong>comunicativo</strong>&nbsp;– il moderno metodo standard</li>
        </ul>



        <p>Ogni metodo ha un diverso obiettivo o priorità, quindi vediamo come queste differenze si riflettono in
            termini pratici in aula.</p>



        <p><strong>Metodo grammaticale-traduttivo</strong></p>



        <p>Il metodo classico è stato uno dei primi metodi di insegnamento delle lingue straniere. Si concentra
            sull’apprendimento delle regole grammaticali e sulla traduzione tra la lingua madre e la lingua straniera.
            Questo approccio è ampiamente utilizzato nelle scuole tradizionali, esercita un certo fascino su coloro che
            sono interessati alle lingue da una&nbsp;prospettiva intellettuale o linguistica.&nbsp;Tale metodo
            grammaticale-traduttivo fornisce una&nbsp;solida base grammaticale&nbsp;e mette l’accento sulla precisione
            nell’uso della lingua. Gli studenti imparano a comprendere le strutture grammaticali e ad applicarle
            correttamente. Tuttavia, questo metodo può risultare&nbsp;noioso&nbsp;e&nbsp;poco coinvolgente per gli
            studenti. L’attenzione eccessiva sulla grammatica e sulla traduzione è certamente d’ostacolo allo sviluppo
            delle abilità comunicative e della fluidità linguistica.</p>



        <p><strong>Metodo diretto</strong></p>



        <p>Il metodo diretto noto anche come ‘metodo orale‘ o ‘metodo naturale‘, si è sviluppato nei primi anni del 1900
            come alternativa al tradizionale metodo grammaticale-traduttivo. Questo metodo&nbsp;si basa sull’uso
            esclusivo della lingua straniera durante le lezioni. Gli insegnanti evitano di utilizzare la lingua madre
            dello studente e cercano di creare un ambiente immersivo nella lingua oggetto dello studio.</p>



        <p>L’attenzione nel corso delle lezioni è concentrata su una buona pronuncia, spesso illustrando agli studenti i
            simboli fonetici prima degli esempi di scrittura standard. Si punta molto sull’interazione orale e sull’uso
            spontaneo della lingua, non sulla traduzione e poco sull’analisi delle regole grammaticali e della sintassi.
        </p>



        <p>Evidentemente questo approccio&nbsp;favorisce l’acquisizione naturale della lingua,&nbsp;poiché gli studenti
            sono costretti a pensare direttamente nella lingua straniera.&nbsp;Promuove l’ascolto attivo&nbsp;e lo
            sviluppo dell’abilità comunicativa.</p>



        <p>Tuttavia, tale modalità d’apprendimento potrebbe risultare&nbsp;difficile per gli studenti
            principianti&nbsp;per comprendere le istruzioni senza l’uso della lingua madre. Inoltre,&nbsp;può richiedere
            più tempo&nbsp;per l’apprendimento, poiché i concetti devono essere spiegati solo tramite la lingua target.
            Necessita di classi con pochi studenti, che abbiano una forte motivazione, per garantire una pratica
            sufficiente ad ogni studente.</p>



        <p><strong>Metodo Audio-orale (o Army Method)</strong></p>



        <p>Il metodo audio-orale enfatizza l’ascolto e la ripetizione orale. Gli studenti ascoltano e ripetono frasi e
            dialoghi, concentrandosi sulla pronuncia corretta e sull’intonazione. Il metodo, è stato sviluppato durante
            la Seconda Guerra Mondiale, quando gli eserciti avevano bisogno di acquisire dimestichezza, dal punto di
            vista orale, con le lingue degli alleati e dei nemici nel più breve tempo possibile.&nbsp;Fu la prima ad
            essere basata sulla teoria linguistica e sulla psicologia comportamentale.&nbsp;Il metodo audio-orale era
            ampiamente utilizzato negli anni ’50 e ’60 e l’enfasi non era posta sulla comprensione delle parole, ma
            piuttosto&nbsp;sull’acquisizione di strutture e modelli per la conversazione della vita di tutti i giorni.
            Questi modelli sono ricavati, ripetuti e testati fino a che le risposte date dagli studenti nella lingua
            straniera diventano&nbsp;automatiche</p>



        <p>Di tutta evidenza questo metodo&nbsp;sviluppa l’abilità di comprendere e riprodurre correttamente&nbsp;i
            suoni della lingua. Gli studenti imparano a&nbsp;riconoscere modelli e strutture
            linguistiche&nbsp;attraverso l’ascolto attivo.</p>



        <p>Detto approccio audio-orale finisce per&nbsp;trascurare l’aspetto scritto e la grammatica. Gli studenti
            potrebbero avere&nbsp;difficoltà a comprendere la struttura della lingua&nbsp;e ad applicarla correttamente
            nella scrittura.</p>



        <p><strong>Metodi umanistico-affettivo</strong></p>



        <p>Negli anni ’70 sotto l’onda della diffusione dello studio delle lingue, si è verificata&nbsp;un’esplosione di
            nuovi e radicali approcci&nbsp;per l’apprendimento di una lingua. Questi approcci sono spesso raggruppati
            nella definizione insegnamento umanistici-affettivi in base al metodo di concentrazione, che fa leva sulle
            capacità innate che si pensa abbiano tutti gli studenti. I metodi umanistico-affettivo&nbsp;si concentrano
            sull’apprendimento emotivo&nbsp;e&nbsp;sulla motivazione degli studenti. Mettono l’accento sulla relazione
            tra l’insegnante e lo studente, creando un ambiente positivo e incoraggiante.</p>



        <p>I principali approcci umanistici-affettivi sono:</p>



        <ul>
            <li><strong>Suggestopedia</strong></li>
        </ul>



        <p>Questo metodo si basa sull’idea che la mente dispone di un enorme potenziale e può immagazzinare informazioni
            tramite la suggestione. Questo metodo di insegnamento utilizza il&nbsp;rilassamento&nbsp;come strumento per
            memorizzare nuove conoscenze.</p>



        <ul>
            <li><strong>Total Physical Response (TPR)</strong></li>
        </ul>



        <p>Questo metodo&nbsp;fa ricorso ai principi base secondo cui i bambini imparano la loro prima lingua.
            Sviluppato da James Asher, questo metodo di insegnamento implica un’ampia gamma di attività fisiche e molte
            attività di ascolto e comprensione, oltre a porre l’enfasi sull’apprendimento come divertimento&nbsp;e
            stimolo.</p>



        <ul>
            <li><strong>Metodo Silent Way</strong></li>
        </ul>



        <p>Con questa tecnica l’insegnante rimane praticamente in silenzio (da qui il nome del metodo) evitando di
            spiegare ogni cosa agli studenti. Questo metodo si basa su un approccio&nbsp;all’apprendimento fondato sulla
            risoluzione dei problemi, in cui lo studio da parte degli studenti&nbsp;diventa autonomo e cooperativo.</p>



        <ul>
            <li><strong>Community Language Learning</strong></li>
        </ul>



        <p>Questa tecnica ha avuto una vita relativamente breve, pur condividendo molti dei principi del metodo Silent
            Way. Considerando lo studente come una persona ‘completa’, il metodo implica che gli studenti&nbsp;si
            siedano in circolo&nbsp;e siano incoraggiati a&nbsp;utilizzare le proprie sensazioni, l’intelletto, le
            relazioni e le reazioni.</p>



        <p>Tutti i vari approcci sono indicati hanno l’obiettivo di suscitare un ambiente di apprendimento positivo, in
            cui gli studenti si sentono motivati e supportati dov peraltro la motivazione intrinseca è essenziale per
            l’apprendimento efficace delle lingue straniere. Essi sono meno strutturati e meno focalizzati sugli
            obiettivi di apprendimento specifici. Alcuni studenti potrebbero richiedere un maggiore livello di struttura
            e guida per raggiungere risultati concreti.</p>



        <p><strong>Metodo comunicativo</strong></p>



        <p>Il metodo comunicativo si concentra sull’uso effettivo della lingua in situazioni reali&nbsp;di
            comunicazione. Gli studenti partecipano ad attività che mirano a sviluppare le abilità linguistiche in modo
            contestuale e significativo.</p>



        <p>Questo approccio&nbsp;promuove l’abilità di comunicare&nbsp;in modo efficace nella lingua straniera. Gli
            studenti imparano a interagire autenticamente e ad applicare le loro conoscenze linguistiche in contesti
            reali. Per contro potrebbe esserci una&nbsp;mancanza di enfasi sulla grammatica&nbsp;e sulla struttura della
            lingua. Alcuni studenti potrebbero avere difficoltà a comprendere le regole grammaticali o a esprimere
            concetti complessi senza una solida base di conoscenza grammaticale.</p>



        <p><strong>Concludendo</strong></p>



        <p>L’insegnamento fatto dai docenti della <strong>Libera Università della Svizzera</strong> delle lingue
            straniere può beneficiare di una varietà di metodi e approcci, ciascuno con i suoi pro e contro
            specifici.&nbsp;</p>



        <p><strong>Non esiste un metodo universale che sia efficace per tutti gli studenti in ogni
                contesto</strong>.&nbsp;</p>



        <p>I nostri insegnanti considerano le esigenze e le preferenze degli studenti, nonché il contesto di
            apprendimento, al fine di&nbsp;<strong>scegliere e combinare gli elementi più appropriati da diversi
                approcci</strong>.&nbsp;Un approccio flessibile ed eclettico potrebbe fornire un equilibrio tra
            l’acquisizione delle competenze linguistiche e la comunicazione autentica, garantendo l’apprendimento.</p>



        <p>Le lingue insegnate sono principalmente <strong>l’inglese, il francese, il russo ed il cinese</strong>.</p>
