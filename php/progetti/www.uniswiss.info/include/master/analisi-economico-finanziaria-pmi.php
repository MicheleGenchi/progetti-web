                <li>redigere e interpretare il Bilancio consolidato dei gruppi quotati e non</li>
                <li>comprendere e valutare l’impatto della variabile fiscale nell’economia e nella gestione delle imprese</li>
                <li>Comprendere i fondamenti della finanza di impresa e del quadro normativo della finanza sostenibile.</li>
