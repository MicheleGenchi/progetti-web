        <div>
            <p>Il corso sul Mobility Manager è mirato a formare soggetti capaci di contribuire alla&nbsp; redazione ed attuazione dei piani di mobilità aziendale (anche con corsi tecnici di formazione), supportare i Dipartimenti di Mobilità nell’istruttoria dei progetti cofinanziati, eseguire il monitoraggio dei servizi e l’analisi delle rendicontazioni tecnico-economiche), organizzare convegni tematici, workshop, tavoli di quadrante e sviluppare iniziative per la diffusione di car sharing, car pooling, smart working.</p>
            <p>In particolare l’obiettivo è formare una risorsa capace di collabora con gli altri Mobility Manager aziendali e scolastici per la</p>
            <ul>
                <li>la promozione del ruolo dei Mobility Manager di Azienda</li>
                <li>l&#8217;incentivazione dei Mobility Manager di Azienda alla partecipazione attiva</li>
                <li>l&#8217;attività di formazione</li>
                <li>l&#8217;attività e progetti che aggreghino Aziende in azioni comuni di mobilità sostenibile</li>
                <li>lo sviluppo di progetti innovativi per smart working, car pooling, car sharing aziendale, mobilità elettrica eccetera</li>
            </ul>
            <p>e sostenere inoltre:</p>
            <ul>
                <li>l&#8217;assistenza tecnica</li>
                <li>l&#8217;assistenza informatica (anche a distanza)</li>
                <li>l&#8217;analisi dei dati forniti dai mobility manager aziendali</li>
                <li>la certificazione e monitoraggio dei piani mobilità aziendali (PMA)</li>
            </ul>
