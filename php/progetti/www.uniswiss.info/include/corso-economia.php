    <img src="images/economia.jpg" alt="" class="img-fluid mt-3">
    <p class="mt-3">Il Corso in "Scienze economiche e delle organizzazioni aziendali" (<i>Economics and Business Organizations</i>) si propone di formare laureati idonei ad operare con <b>funzioni di responsabilità nell'ambito di imprese pubbliche e private</b> e ad esercitare, previo superamento del relativo esame di stato, la libera professione. In particolare il laureato triennale potrà, nella sfera pubblica, inserirsi nell'organico di enti ed istituzioni statali o di organizzazioni "non profit" con accesso ai livelli impiegatizi elevati corrispondenti alla formazione specialistica conseguita; nella sfera privata, potrà assumere il profilo di un quadro d'impresa che collabora strettamente con l'imprenditore e il management o <b>svolgere mansioni connesse ad attività di gestione, amministrazione e controllo aziendale</b>.
    </p>
    <p>
    <b>Nel primo anno</b> sono previsti insegnamenti finalizzati ad offrire conoscenze di base e metodologie di analisi relative a <b>discipline economiche, aziendali, quantitative e giuridiche</b>. Nei <b>due anni successivi</b> si intende fornire una adeguata conoscenza delle <b>discipline aziendali</b> attraverso l'attivazione di un ampio ventaglio di insegnamenti articolati sia in lezioni frontali che in seminari di approfondimento ed analisi di specifici casi aziendali.</p>
