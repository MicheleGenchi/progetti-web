        <h2 class="intro"><strong>CARATTERISTICHE DELL’ATTIVITÀ DIDATTICA</strong></h2>
<hr><br>

        <p>Ogni insegnamento, in applicazione del modello formativo, realizza la didattica erogativa (DE) attraverso le lezioni preregistrate in un formato interattivo. Inoltre, svolge attività didattiche a quadro orario, assimilabili alla didattica frontale, in web-conference su apposita piattaforma; tali attività si concretizzano nella presentazione-illustrazione di argomenti e temi secondo il Programma dello stesso insegnamento (ad esempio, lezioni frontali, attività laboratoriali on line e discussione di prove di valutazione intermedia).</p>

        <p>Le attività di didattica erogativa, quindi, sono attività formative per gli studenti che si caratterizzano nel modo seguente per la offerta di strumenti e materiale prodotti per realizzare un apprendimento significativo differenziato, cioè sia teorico sia pratico.</p>

        <p>L’offerta, dunque, è finalizzata alla formazione autodiretta dagli studenti orientata verso la determinazione dei risultati di apprendimento previsti dal piano degli studi. I risultati di apprendimento sono esplicitati nel programma dell’insegnamento attraverso: gli obiettivi formativi correlati a quelli generali dell’insegnamento; il collegamento tra temi e argomenti disciplinari presenti nel materiale standard; il collegamento tra contenuti e prove di autovalutazione.</p>

        <p>La didattica erogativa, inoltre, prevede anche attività formative supplementari consistenti in lezioni in presenza, ovvero in web-conference, programmate a quadro orario calendarizzato, che si realizzano o replicano, nei singoli periodi didattici. Per tali lezioni, sono esplicitati: o gli obiettivi formativi correlati a quelli generali dell’insegnamento; o il collegamento con gli argomenti disciplinari nel Programma di studio; o il collegamento con le prove di autovalutazione.</p>

        <p>Infine, sono previste anche attività di tirocinio realizzate presso o enti esterni all’Università, riconosciuti, oppure presso soggetti consociati con l’Università.</p>

        <p>Ogni insegnamento, inoltre, realizza la didattica interattiva (DI) in aula virtuale o in web-conference; sono realizzati interventi formativi, svolti anche dal tutor disciplinare, che si caratterizzano come didattica attiva e partecipativa situata on line. Ogni insegnamento concretizza tale didattica con una focalizzazione o di nuove dimostrazioni o di suggerimenti operativi collegati alla presentazione-illustrazione di problemi, di simulazioni, di esercizi, di soluzioni operative, di modalità di realizzazione di artefatti e di attività di tipo progettuale, ecc.</p>

        <p>Le attività di didattica interattiva assolvono a quanto previsto dal modello formativo e in particolare sono attuate per facilitare l’apprendimento esperienziale (Experiential Learning) cioè apprendimento basato sull’esperienza cognitiva, emotiva o sensoriale.</p>

        <p>Le azioni e le attività rispondenti sono:</p>

        <ul style="text-align:left">

          <li>Attività strutturate, realizzate come situational problem solving o case study, con risultati espressi sotto forma di project work, report, relazioni, produzione di artefatti, ecc.</li>

          <li>Interventi didattici di gruppo, in web-conference, programmati a quadro orario settimanale, calendarizzato, per attività didattiche di spiegazione su contenuti, dimostrazioni, svolgimento di esemplificazioni, di simulazioni, attività di valutazione progressiva.</li>

          <li>Attività Progettuali. Riguardanti aspetti dell’insegnamento disciplinare e funzionali all’acquisizione di autonomia, responsabilità, problem solving, critica costruttiva, capacità di ragionamento e competenza da parte dagli studenti sotto la supervisione del docente o del tutor.</li>

          <li>Attività situazionali. La sostituzione di alcune delle attività di didattica in presenza è realizzata tramite l’ambientazione e la contestualizzazione di compiti autentici (tramite activity o progetti) per la cui soluzione sono proposte e fatte utilizzare tecniche attive come: Simulazione (role play, copioni, …), Analitiche (studi di caso /auto caso/ autobiografie), Problematiche (situazioni critiche/incident, …), Proattive (brainstorming, progetti…), Relazional (cooperative learning, peer tutoring), Esercitative.</li>

        </ul>

        <p>In ogni insegnamento il docente, coadiuvato dal tutor disciplinare, per rendere significativo lo svolgimento della didattica interattiva, svolge una costante sollecitazione agli studenti per coinvolgerli nelle attività della didattica interattiva.</p>

        <p>Queste azioni di sollecitazione e coinvolgimento sono rivolte alla costituzione di comunità di pratica non soltanto legate alla disciplina, bensì a tutto il corso di studio.</p>

        <p>La Libera Università della Svizzera si distingue per un&nbsp;modello didattico innovativo, basato su contenuti digitali sincroni (lezioni live, masterclasses) e asincroni (video, quiz, esercizi). Questo modello permette agli studenti di accedere ai percorsi di formazione in qualunque momento e da qualunque luogo, garantendo al contempo una formazione di alta qualità.</p>

        <p>Il modello didattico della libera Università della Svizzera è pensato per rispondere alle esigenze delle nuove generazioni di studenti, caratterizzate da un bisogno di flessibilità e inclusività.</p>

        <p>A tal proposito, l’istituto si impegna a fornire un’educazione che prepari gli studenti a entrare nel mondo del lavoro con un background di competenze in linea con la domanda. Questo significa non solo insegnare le competenze tecniche necessarie, ma anche sviluppare le soft skills che sono sempre più richieste dalle aziende.</p>

        <p>In questo senso, la Libera Università della Svizzera rappresenta un vero modello innovativo di istituzione accademica, che mette al centro le esigenze degli studenti e del mercato del lavoro, puntando a formare professionisti pronti a rispondere alle sfide del futuro.</p>

